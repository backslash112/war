enum PhoneType: Int {
    case Unknown, iPhone4, iPhone5, iPhone6, iPhone6Plus
    
    func description() -> String {
        switch self {
        case .Unknown:
            return "unknown"
            
        case .iPhone4:
            return "iPhone 4"
        
        case .iPhone5:
            return "iPhone 5"
        
        case .iPhone6:
            return "iPhone 6"
        
        case .iPhone6Plus:
            return "iPhone 6+"
        }
    }
}

class WARDevice {

    class func getPhoneType() -> PhoneType {
        switch UIScreen.mainScreen().bounds.size.height {
        case 480:
            return .iPhone4
            
        case 568:
            return .iPhone5
            
        case 667:
            return .iPhone6
            
        case 736:
            return .iPhone6Plus
            
        default:
            return .Unknown
        }
    }
    
    class func getPhoneVersion() -> Double {
        return NSString(string: UIDevice.currentDevice().systemVersion).doubleValue
    }
    
    class func isiOS8() -> Bool {
        return getPhoneVersion() >= 8
    }
    
    class func isiOS7() -> Bool {
        return getPhoneVersion() >= 7  && getPhoneVersion() < 8
    }
}
